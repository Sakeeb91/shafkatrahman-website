#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const manifestPath = path.join(root, 'writing-manifest.js');
const outputPath = path.join(root, 'writing-graph-data.js');
const checkOnly = process.argv.includes('--check');

function fail(message) {
    console.error(`Writing graph validation failed: ${message}`);
    process.exitCode = 1;
}

function loadManifest() {
    const sandbox = { window: {} };
    vm.runInNewContext(fs.readFileSync(manifestPath, 'utf8'), sandbox, { filename: manifestPath });
    return Array.from(sandbox.window.WRITING_MANIFEST || [], (entry) => ({
        ...entry,
        tags: Array.from(entry.tags || []),
        related: Array.from(entry.related || [])
    }));
}

function tagId(tag) {
    return `tag:${tag.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '')}`;
}

function articleId(articlePath) {
    return `article:${articlePath}`;
}

function resolveArticleFile(articlePath) {
    return path.join(root, articlePath.replace(/^\//, ''));
}

function extractInternalLinks(article, knownPaths) {
    const html = fs.readFileSync(resolveArticleFile(article.path), 'utf8');
    const links = new Set();
    const pattern = /href\s*=\s*["']([^"'#]+)(?:#[^"']*)?["']/gi;
    let match;

    while ((match = pattern.exec(html))) {
        try {
            const resolved = new URL(match[1], `https://shafkatrahman.com${article.path}`).pathname;
            if (resolved !== article.path && knownPaths.has(resolved)) links.add(resolved);
        } catch {
            // Ignore malformed and non-HTTP links. HTML validation handles them separately.
        }
    }
    return Array.from(links).sort();
}

function generateGraph(manifest) {
    const paths = new Set();
    const titles = new Set();
    const tags = new Map();

    for (const article of manifest) {
        if (!article.path || !article.title || !article.date || !article.type) fail(`incomplete manifest entry ${JSON.stringify(article)}`);
        if (paths.has(article.path)) fail(`duplicate article path ${article.path}`);
        if (titles.has(article.title)) fail(`duplicate article title ${article.title}`);
        if (!['Essay', 'Research'].includes(article.type)) fail(`unsupported article type ${article.type} for ${article.path}`);
        if (!Array.isArray(article.tags) || article.tags.length === 0) fail(`missing tags for ${article.path}`);
        if (!fs.existsSync(resolveArticleFile(article.path))) fail(`missing article file ${article.path}`);
        paths.add(article.path);
        titles.add(article.title);
        article.tags.forEach((tag) => {
            const id = tagId(tag);
            if (tags.has(id) && tags.get(id) !== tag) fail(`tag slug collision between "${tags.get(id)}" and "${tag}"`);
            tags.set(id, tag);
        });
    }

    for (const article of manifest) {
        for (const relatedPath of article.related) {
            if (!paths.has(relatedPath)) fail(`unknown related path ${relatedPath} in ${article.path}`);
            if (relatedPath === article.path) fail(`self relationship in ${article.path}`);
        }
    }

    if (process.exitCode) return null;

    const nodes = [
        ...manifest.map((article) => ({
            id: articleId(article.path),
            type: 'article',
            label: article.title,
            path: article.path,
            articleType: article.type,
            date: article.date
        })),
        ...Array.from(tags, ([id, label]) => ({ id, type: 'tag', label }))
    ];
    const edges = [];
    const edgeKeys = new Set();

    function addEdge(source, target, type) {
        const endpoints = [source, target].sort();
        const key = `${type}:${endpoints[0]}:${endpoints[1]}`;
        if (edgeKeys.has(key)) return;
        edgeKeys.add(key);
        edges.push({ source, target, type });
    }

    for (const article of manifest) {
        const source = articleId(article.path);
        article.tags.forEach((tag) => addEdge(source, tagId(tag), 'tag'));
        extractInternalLinks(article, paths).forEach((target) => addEdge(source, articleId(target), 'internal'));
        article.related.forEach((target) => addEdge(source, articleId(target), 'related'));
    }

    nodes.sort((a, b) => a.id.localeCompare(b.id));
    edges.sort((a, b) => `${a.type}:${a.source}:${a.target}`.localeCompare(`${b.type}:${b.source}:${b.target}`));

    return {
        version: 1,
        generatedFrom: 'writing-manifest.js',
        nodes,
        edges
    };
}

const graph = generateGraph(loadManifest());
if (!graph) process.exit(1);

const output = `/* Generated by scripts/generate-writing-graph.mjs. Do not edit. */\nwindow.WRITING_GRAPH = Object.freeze(${JSON.stringify(graph, null, 2)});\n`;

if (checkOnly) {
    if (!fs.existsSync(outputPath) || fs.readFileSync(outputPath, 'utf8') !== output) {
        fail('writing-graph-data.js is stale; run node scripts/generate-writing-graph.mjs');
    } else {
        console.log(`Writing graph is current: ${graph.nodes.length} nodes, ${graph.edges.length} edges.`);
    }
} else {
    fs.writeFileSync(outputPath, output);
    console.log(`Generated writing-graph-data.js with ${graph.nodes.length} nodes and ${graph.edges.length} edges.`);
}
